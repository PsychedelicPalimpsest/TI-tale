  ld a, (phase)
  ld b, a
  inc a
  cp 3
  jp nz, phase_not_zero
  xor a, a
phase_not_zero:
  ld (phase), a

  ld a, b ; Restore pre inc phase

; A = 12 * A
  add a, a 
  add a, a 

  ld b, a
  add a, a
  add a, b

; BC = 0x00{AA}
  ld b, $0 
  ld c, a

  ld hl, phase_table
  add hl, bc

  ld bc, hl ; bc = phase table

  ld de, (_current_light_buff)  ; bc = light table
  ld hl, (_current_dark_buff)   ; hl = dark table

    ld a, $1 ; 8bit mode
    out (10h), a

    ld a, $7 ; Move right
    out (10h), a


    
    ld ixh, 80h  ; ixh = row + 80h
row_loop:
    ld a, ixh ; Set col (saved in d)
    out (10h), a


    ld a, 20h ; Go to beginning of col
    out (10h), a

MACRO do_pix
; PD + P'L
  ld a, (bc)
  and (hl)
  ld ixl, a ; save  PD in iyl

  ex de, hl ; hl is now the light table
  ld a, (bc)
  cpl a
  and (hl) 
  ex de, hl ; restore hl to be dark

  or ixl

  inc de
  inc hl
  inc bc

  out (11h), a
ENDM

; Unrolled write loop (12 entries)
