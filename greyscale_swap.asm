PUBLIC _greyscale_swap

extern _working_dark_buff
extern _working_light_buff

extern grey_phase1_buffer
extern grey_phase2_buffer
extern grey_phase3_buffer

_greyscale_swap:
  
  
  ld iy, grey_phase1_buffer
  ld bc, phase_table
  call greyscale_swap_single

  ld iy, grey_phase2_buffer
  ld bc, phase_table + 1
  call greyscale_swap_single 

  ld iy, grey_phase3_buffer
  ld bc, phase_table + 2
  jp greyscale_swap_single


; Inputs:
;  bc is the input phase table entry
;  iy is the output phase buffer
greyscale_swap_single:
  ld hl, _working_dark_buff
  ld de, _working_light_buff

  ld ixh, 24


MACRO PIXEL_8
  ld a, (bc)
  and (hl)
  ld ixl, a

  inc hl
  ex de, hl

  ld a, (bc)
  cpl a
  and (hl)
  or ixl

  inc hl
  ex de, hl

  ld (iy), a

  inc iy
  inc bc
endm

